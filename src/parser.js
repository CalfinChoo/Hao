const Tile = function(x, y, passable, spawn, checkpoint, death, name, sprite, size, endpoint, message){
  this.x = x;
  this.y = y;
  this.passable = passable;
  this.isSpawn = spawn;
  this.isCheckpoint = checkpoint;
  this.isDeath = death;
  this.name = name;
  this.sprite = sprite;
  this.size = size;
  this.triggered = false;
  this.isFinish = endpoint;
  this.isMessage = message;

  this.getX = function() {
    return this.x;
  };
  this.getY = function() {
    return this.y;
  };
  this.getPassable = function() {
    return this.passable;
  };
  this.getSpawn = function() {
    return this.isSpawn;
  };
  this.getDeath = function() {
    return this.isDeath;
  };
  this.getCheckpoint = function() {
    return this.isCheckpoint;
  };
  this.getTriggered = function() {
    return this.triggered;
  };
  this.setTriggered = function(e) {
    this.triggered = e;
    return true;
  };
  this.getFinish = function() {
    return this.isFinish;
  };
  this.getMessage = function() {
    return this.isMessage;
  };
};

